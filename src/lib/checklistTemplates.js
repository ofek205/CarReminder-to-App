/**
 * Return a fresh (cloned) sections array for the given phase + engine type.
 * Caller is responsible for assigning ids + wrapping items in `{id,text,checked}`.
 * Shape returned: [{ name, items: string[] }]
 */
export function getDefaultSections(phase, engineType = 'outboard') {
  const src =
    phase === 'engine' ? (engineType === 'inboard' ? ENGINE_INBOARD : ENGINE_OUTBOARD) :
    phase === 'pre'    ? PRE_DEFAULTS :
    phase === 'post'   ? POST_DEFAULTS :
    [];
  return src.map(s => ({ name: s.name, items: s.items.slice() }));
}
